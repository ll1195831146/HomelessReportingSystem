    public ManageEnterpriseJPanel(JPanel userProcessContainer, EcoSystem system) {
        initComponents();
        this.userProcessContainer = userProcessContainer;
        this.system = system;

        this.allcity = new ArrayList<>();
        for(Country country: system.getCountryList()){
            for (State state :country.getStatelist()){
                for(City c:state.getCitylist()){
                    allcity.add(c);
                    
                }
            }
        }
        populateTable();
        populateComboBox();
    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();

        model.setRowCount(0);
        for(City city : allcity){
        
            for (Enterprise enterprise : city.getEnterprisedirectory().getEnterpriselist()) {
                Object[] row = new Object[3];
                row[0] = enterprise;
                row[1] = enterprise.getCity().getName();
                row[2] = enterprise.getEnterprisetype().getValue();

                model.addRow(row);
            }
        }
     
    }

    private void populateComboBox() {
        networkJComboBox.removeAllItems();
        enterpriseTypeJComboBox.removeAllItems();

        for (City city : allcity) {
            networkJComboBox.addItem(city);
        }

        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            enterpriseTypeJComboBox.addItem(type);
        }

    }

