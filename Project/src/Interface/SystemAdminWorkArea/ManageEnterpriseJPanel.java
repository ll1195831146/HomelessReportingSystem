    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        City city = (City) networkJComboBox.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) enterpriseTypeJComboBox.getSelectedItem();

        if (city == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }

        String name = nameJTextField.getText();
        for (City c:allcity){
            for (Organization o :c.getEnterprisedirectory().getEnterpriselist()){
                if (name.equals(o.getName())){
                    
                JOptionPane.showMessageDialog(null, "Same Name Exist!!"); 
                return;
                }
            }
        }
        Enterprise enterprise = city.getEnterprisedirectory().createAndAddEnterprise(name, type, city);
        JOptionPane.showMessageDialog(null, "Add Successfully");
        populateTable();

    }//GEN-LAST:event_submitJButtonActionPerformed

