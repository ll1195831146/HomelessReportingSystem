    public ManageEnterpriseAdminJPanel(JPanel userProcessContainer, EcoSystem system) {
        initComponents();

        this.userProcessContainer = userProcessContainer;
        this.system = system;

        this.allcity = new ArrayList<>();
        
        for(Country country: system.getCountryList()){
             for (State state :country.getStatelist()){
                for(City c:state.getCitylist()){
                    allcity.add(c);
                    
                }
            }
        }
        populateTable();
        populateNetworkComboBox();
    }

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblEnterprise.getModel();

        model.setRowCount(0);
        for (City city : allcity) {
            for (Enterprise enterprise : city.getEnterprisedirectory().getEnterpriselist()) {
                for (UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccountList()) {
                    Object[] row = new Object[3];
                    row[0] = enterprise.getName();
                    row[1] = city.getName();
                    row[2] = userAccount.getUsername();

                    model.addRow(row);
                }
            }
        }
    }

    private void populateNetworkComboBox(){
        networkJComboBox.removeAllItems();
        
        for (City city : allcity){
            networkJComboBox.addItem(city);
        }
    }
    
    private void populateEnterpriseComboBox(City city){
        enterpriseJComboBox.removeAllItems();
        
        for (Enterprise enterprise : city.getEnterprisedirectory().getEnterpriselist()){
            enterpriseJComboBox.addItem(enterprise);
        }
        
    }
    
