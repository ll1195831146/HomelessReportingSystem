    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedrow =enterpriseJTable.getSelectedRow();
        City city = (City) networkJComboBox.getSelectedItem();
        
        if(selectedrow >=0){
            Enterprise enterprise = (Enterprise)enterpriseJTable.getValueAt(selectedrow, 0);
            city.getEnterprisedirectory().getEnterpriselist().remove(enterprise);
            JOptionPane.showMessageDialog(null,"Enterprise has been deleted");
            populateTable();
        }
        else 
            JOptionPane.showMessageDialog(null,"Please select any row");
    }//GEN-LAST:event_btnDeleteActionPerformed

